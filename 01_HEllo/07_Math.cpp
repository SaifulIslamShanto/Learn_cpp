#include <iostream>
#include <math.h>
using namespace std;

int main()
{
    int a = 2;
    cout << "sin " << sin(a) << endl;

    cout << "Cos : " << cos(a) << endl;
    cout << "tan : " << tan(a) << endl;
    cout << "Square Root :  " << sqrt(a) << endl;
    cout << "power :  " << pow(2, 3) << endl;
}